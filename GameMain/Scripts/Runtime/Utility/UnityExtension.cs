    /// 获取或增加组件。
    /// </summary>
    /// <typeparam name="T">要获取或增加的组件。</typeparam>
    /// <param name="gameObject">目标对象。</param>
    /// <returns>获取或增加的组件。</returns>
    public static T GetOrAddComponent<T>(this GameObject gameObject) where T : Component
    {
        T component = gameObject.GetComponent<T>();
        if (component == null)
        {
            component = gameObject.AddComponent<T>();
        }

        return component;
    }

    /// <summary>
    /// 获取或增加组件。
    /// </summary>
    /// <param name="gameObject">目标对象。</param>
    /// <param name="type">要获取或增加的组件类型。</param>
    /// <returns>获取或增加的组件。</returns>
    public static Component GetOrAddComponent(this GameObject gameObject, Type type)
    {
        Component component = gameObject.GetComponent(type);
        if (component == null)
        {
            component = gameObject.AddComponent(type);
        }

        return component;
    }

    /// <summary>
    /// 设置物品数量显示的文本
    /// </summary>
    /// <param name="text">文本</param>
    /// <param name="num">物品数量</param>
    /// <param name="type">物品类型</param>
    /// <param name="tight">字符是否紧凑</param>
    public static void SetItemText(this TextMeshProUGUI text, int num, TotalItemData type, bool tight)
    {
        if (type == TotalItemData.Coin)
        {
            text.SetText(num.ToString());
        }
        else if (type == TotalItemData.InfiniteLifeTime || type == TotalItemData.InfiniteMagnifierBoost || type == TotalItemData.InfiniteAddOneStepBoost || type == TotalItemData.InfiniteFireworkBoost) 
        {
            if (tight)
            {
                if (num < 60)
                    text.SetText(num + "m");
                else
                    text.SetText(num / 60f + "h");
            }
            else
            {
                if (num < 60)
                    text.SetText(num + " m");
                else
                    text.SetText(num / 60f + " h");
            }
        }
        else
        {
            if (tight)
            {
                text.SetText("x" + num.ToString());
            }
            else
            {
                text.SetText("x " + num.ToString());
            }
        }
    }

