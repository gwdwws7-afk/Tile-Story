    /// <summary>
    /// 设置红色数量提示文本
    /// </summary>
    /// <param name="text">文本</param>
    /// <param name="num">提示数量</param>
    public static void SetRedNumSignText(this TextMeshProUGUI text, int num)
    {
        if (num < 10)
        {
            text.fontSize = 54;
        }
        else
        {
            text.fontSize = 44;
        }

        text.SetText(num.ToString());
    }

